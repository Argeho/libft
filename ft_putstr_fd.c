#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	loc;

	loc = 0;
	if (s != 0)
	{
		while (s[loc] != '\0')
		{
			ft_putchar_fd(s[loc], fd);
			loc++;
		}
	}
}
